
public class Pair
{
    
    int row, collumn, value;
    
    public Pair(int x, int y, int v)
    {
        row = x;
        collumn = y;
        value = v;
        
    }
    
    public int getRow()
    {
        return row;
    }
    
    public int getCollumn()
    {
        return collumn;
    }
    
    public int getValue()
    {
        return value;
    }
}
